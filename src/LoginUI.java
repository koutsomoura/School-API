    public boolean existsInTable(JTable table, String s) {

        for (int i = 0; i < table.getRowCount(); i++) {
            if ((table.getValueAt(i, 0).toString()).equalsIgnoreCase(s)) {
                return false;
            }
        }
        return true;
    }
